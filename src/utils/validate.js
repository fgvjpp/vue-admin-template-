/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path)
}

/**
 * @param {string} str
 * @returns {Boolean}
 */

// 校验手机号
export function validUsername(str) {
  const iphone = /^(13[0-9]|14[01456879]|15[0-35-9]|16[2567]|17[0-8]|18[0-9]|19[0-35-9])\d{8}$/
  return iphone.test(str)
}

// 校验密码
export function validUserPwd(pwd) {
  return pwd.length >= 6 && pwd.length <= 16
}
